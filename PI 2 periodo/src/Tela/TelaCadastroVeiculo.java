    private void jButtonBuscarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarCActionPerformed
        try {
            ClienteDao lista = new ClienteDao();
            DefaultTableModel model = (DefaultTableModel) jTable_saidaV.getModel();

            ArrayList<cliente> listaCliente = null;
            listaCliente = lista.obterClientes();
            String[] vetString = new String[9];
            String saida = "";
            model.setRowCount(0);
            boolean contem = false;
            for (int i = 0; i < listaCliente.size(); i++) {

                vetString[0] = listaCliente.get(i).getNome() + "";
                vetString[1] = listaCliente.get(i).getTelefone() + "";
                vetString[2] = listaCliente.get(i).getID() + "";
                vetString[3] = listaCliente.get(i).getEndereco() + "";
                vetString[4] = listaCliente.get(i).getEmail() + "";
                vetString[5] = listaCliente.get(i).getTipoCliente() + "";
                vetString[6] = listaCliente.get(i).getCPF_CNPJ() + "";
                vetString[7] = listaCliente.get(i).getDataNascimento() + "";
                vetString[8] = listaCliente.get(i).getRazaoSocial() + "";

                if (jEscolhaV.getSelectedItem().equals("ID")) {
                    if (String.valueOf(listaCliente.get(i).getID()).contains(jtxtBuscar.getText())) {
                        model.addRow(vetString);
                        contem = true;

                    }

                } else if (jEscolhaV.getSelectedItem().equals("Nome")) {
                    if (listaCliente.get(i).getNome().toUpperCase().contains(jtxtBuscar.getText().toUpperCase())) {
                        model.addRow(vetString);
                        contem = true;
                    }

                    //
                } else if (jEscolhaV.getSelectedItem().equals("CPF")) {
                    if (listaCliente.get(i).getCPF_CNPJ().contains(jCPFV.getText())) {
                        model.addRow(vetString);
                        contem = true;
                    }
                } else if (jEscolhaV.getSelectedItem().equals("CNPJ")) {
                    if (listaCliente.get(i).getCPF_CNPJ().contains(jCNPJV.getText())) {
                        model.addRow(vetString);
                        contem = true;
                    }

                    //
                }
            }
            if (contem == false) {
                throw new Exception("Cliente não encontrado");

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
            erro.printStackTrace();
        }
    }//GEN-LAST:event_jButtonBuscarCActionPerformed

