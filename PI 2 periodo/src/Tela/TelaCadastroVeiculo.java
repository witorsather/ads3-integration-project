    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        salvar();
        try {
            veiculoDao metodos = new veiculoDao();
            veiculo cadastarVeiculo = new veiculo();
            marca cadastrarMarca = new marca();
            modelo cadastrarModelo = new modelo();
            
            cadastrarMarca.setDescricao(txtMarca.getText());
            cadastrarMarca.setLogo("...");
            
            cadastrarModelo.setAnoFabricacao(Integer.valueOf(txtAnoFab.getText()));
            cadastrarModelo.setAnoModelo(Integer.valueOf(txtAnoModelo.getText()));
            cadastrarModelo.setDescricao(txtModelo.getText());
            
            cadastarVeiculo.setID(WIDTH);
            cadastarVeiculo.setIdCliente(Integer.valueOf(txtIDCliente.getText()));
            cadastarVeiculo.setMarcaVeiculo(cadastrarMarca);
            cadastarVeiculo.setModeloVeiculo(cadastrarModelo);
            if (!cadastarVeiculo.setPlaca(txtPlaca.getText())) {
                    throw new Exception("Placa Inválida!");
            }
            cadastarVeiculo.setRenavam(txtRenavam.getText());
            if (jCTipo.getSelectedItem().equals("SUV")) {
                cadastarVeiculo.setTipo(tipoModeloVeiculo.SUV);
            } else if (jCTipo.getSelectedItem().equals("ESPORTIVO")) {
                cadastarVeiculo.setTipo(tipoModeloVeiculo.ESPORTIVO);
            } else if (jCTipo.getSelectedObjects().equals("SEDAN")) {
                cadastarVeiculo.setTipo(tipoModeloVeiculo.SEDAN);
            } else {
                cadastarVeiculo.setTipo(tipoModeloVeiculo.RET);
            }
            metodos.incluir(cadastarVeiculo);
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
        

    }//GEN-LAST:event_jBSalvarActionPerformed

