    private void jBAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarActionPerformed
        adicionar();
        try {

            EstoqueDao adicionar = new EstoqueDao();
            estoque adicionarEstoque = new estoque();

            adicionarEstoque.setCodigo(Integer.parseInt(txtCodigoDePecas.getText()));
            adicionarEstoque.setDescricao(txtDescricao.getText());
            adicionarEstoque.setQuantidadePecas(Integer.parseInt(txtQuantidade.getText()));
            adicionarEstoque.setValorUnidade(Float.parseFloat(txtValorU.getText().replace("R$", "").replace(",", ".").trim()));
            adicionarEstoque.setId(Integer.parseInt(txtIDpeca.getText()));

            adicionar.alterar(adicionarEstoque, Integer.parseInt(txtIDpeca.getText()));
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
            erro.printStackTrace();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jBAdicionarActionPerformed

