    private void jButton1EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1EditarActionPerformed
        editar();
        try {
            EstoqueDao metodos = new EstoqueDao();
            ArrayList<estoque> listaEstoque = null;
            listaEstoque = metodos.obterPecas();
            for (int i = 0; i < listaEstoque.size(); i++) {
                if (txtIDpeca.getText().equals(String.valueOf(listaEstoque.get(i).getId()))) {
                    txtCodigoDePecas.setText("" + listaEstoque.get(i).getCodigo());
                    txtDescricao.setText("" + listaEstoque.get(i).getDescricao());
                    txtQuantidade.setText("" + listaEstoque.get(i).getQuantidadePecas());
                    txtValorU.setText("" + listaEstoque.get(i).getValorUnidade());
                }

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
    }//GEN-LAST:event_jButton1EditarActionPerformed


