    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        try {
            EstoqueDao lista = new EstoqueDao();
            DefaultTableModel model = (DefaultTableModel) JtableSaida.getModel();

            ArrayList<estoque> listaEstoque = null;
            listaEstoque = lista.obterPecas();
            String[] vetString = new String[5];
            model.setRowCount(0);
            boolean contem = false;
            for (int i = 0; i < listaEstoque.size(); i++) {

                vetString[0] = listaEstoque.get(i).getDescricao() + "";
                vetString[1] = listaEstoque.get(i).getId() + "";
                vetString[2] = listaEstoque.get(i).getCodigo() + "";
                vetString[3] = listaEstoque.get(i).getQuantidadePecas() + "";
                vetString[4] = listaEstoque.get(i).getValorUnidade() + "";

                if (jctipodeOpcaoDeBusca.getSelectedItem().equals("Código")) {
                    if (String.valueOf(listaEstoque.get(i).getCodigo()).contains(jTextField1campoBusca.getText())) {
                        model.addRow(vetString);
                        contem = true;

                    }

                } else if (jctipodeOpcaoDeBusca.getSelectedItem().equals("ID")) {
                    if (String.valueOf(listaEstoque.get(i).getId()).contains(jTextField1campoBusca.getText())) {
                        model.addRow(vetString);
                        contem = true;
                    }

                } else if (jctipodeOpcaoDeBusca.getSelectedItem().equals("Descrição")) {
                    if (listaEstoque.get(i).getDescricao().toUpperCase().contains(jTextField1campoBusca.getText().toUpperCase())) {
                        model.addRow(vetString);
                        contem = true;
                    }

                }
            }
            if (contem == false) {
                throw new Exception("Peça não encontrada!");

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
            erro.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonBuscarActionPerformed

