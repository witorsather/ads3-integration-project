    private void jButtonexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonexcluirActionPerformed
        try {
            EstoqueDao excluir = new EstoqueDao();
            EstoqueDao lista = new EstoqueDao();
            ArrayList<estoque> listaEstoque = null;
            listaEstoque = lista.obterPecas();
            DefaultTableModel model = (DefaultTableModel) JtableSaida.getModel();
            String[] vetString = new String[9];
            String usuario = "" + JtableSaida.getValueAt(JtableSaida.getSelectedRow(), 1);

            for (int i = 0; i < listaEstoque.size(); i++) {

                if (String.valueOf(listaEstoque.get(i).getId()).equals(usuario)) {

                    excluir.excluir(listaEstoque.get(i).getId());

                }

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }


    }//GEN-LAST:event_jButtonexcluirActionPerformed

    private void JtableSaidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtableSaidaMouseClicked
        try {
            if (attestoque == false) {
                TelaAtualizarEstoque atualizare = new TelaAtualizarEstoque();
                jConsultae.add(atualizare);
                atualizare.setVisible(true);
                atualizare.setPosicao();
                DefaultTableModel model = (DefaultTableModel) JtableSaida.getModel();
                String[] vetString = new String[9];
                String usuario = "" + JtableSaida.getValueAt(JtableSaida.getSelectedRow(), 1);
                EstoqueDao metodos = new EstoqueDao();
                ArrayList<estoque> listaEstoque = null;
                listaEstoque = metodos.obterPecas();
                atualizare.txtIDpeca.setText(usuario);
                for (int i = 0; i < listaEstoque.size(); i++) {
                    if (atualizare.txtIDpeca.getText().equals(String.valueOf(listaEstoque.get(i).getId()))) {
                        atualizare.txtCodigoDePecas.setText("" + listaEstoque.get(i).getCodigo());
                        atualizare.txtDescricao.setText("" + listaEstoque.get(i).getDescricao());
                        atualizare.txtQuantidade.setText("" + listaEstoque.get(i).getQuantidadePecas());
                        atualizare.txtValorU.setText("" + listaEstoque.get(i).getValorUnidade());

                    }

                }

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }


    }//GEN-LAST:event_JtableSaidaMouseClicked


